package com.jisu.gyro;

public class Data {
    private float x,y,z;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }

    public Data(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}

